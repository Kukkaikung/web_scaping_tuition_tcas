# üî¢ ‡πÅ‡∏õ‡∏•‡∏á‡∏Ñ‡πà‡∏≤‡πÉ‡∏ä‡πâ‡∏à‡πà‡∏≤‡∏¢‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô int (‡∏õ‡∏±‡∏î‡πÄ‡∏®‡∏©‡∏Å‡πà‡∏≠‡∏ô‡∏ñ‡πâ‡∏≤‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£)
new_df['‡∏Ñ‡πà‡∏≤‡πÉ‡∏ä‡πâ‡∏à‡πà‡∏≤‡∏¢'] = new_df['‡∏Ñ‡πà‡∏≤‡πÉ‡∏ä‡πâ‡∏à‡πà‡∏≤‡∏¢'].round().astype('Int64')  # ‡πÉ‡∏ä‡πâ Int64 ‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö NaN ‡∏î‡πâ‡∏ß‡∏¢

# ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ü‡∏•‡πå‡πÉ‡∏´‡∏°‡πà
new_df.to_csv("filtered_tcas_cleaned.csv", index=False, encoding='utf-8-sig')

